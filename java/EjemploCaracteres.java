public class EjemploCaracteres {
    public static void main(String[] args) {
        char caracter = 'a';
        System.out.println(caracter);
        caracter = 65;
        System.out.println(caracter);
        caracter = 65 + 1;
        System.out.println(caracter + 1);
        String palabra = "Alura";
        System.out.println(palabra);
        palabra = palabra + " 2023";
        System.out.println(palabra);
    }
}